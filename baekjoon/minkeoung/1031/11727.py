# 입력: 계산할 타일의 가로 길이 n을 입력받음
n = int(input())

# DP 테이블 초기화 (최대 길이 1000까지 계산 가능)
d = [0] * (n + 1)

# 초기값 설정: n=1일 때 1가지 방법, n=2일 때 3가지 방법
d[1] = 1
if n > 1:
    d[2] = 3

# 점화식을 이용하여 DP 테이블 채우기
for i in range(3, n + 1):
    d[i] = d[i - 1] + 2 * d[i - 2]
    d[i] %= 10007  # 값이 커지는 것을 방지하기 위해 10007로 나눈 나머지를 저장

# 결과 출력: n 길이 타일의 경우의 수를 10007로 나눈 나머지 출력
print(d[n])
